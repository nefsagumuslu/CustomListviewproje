package com.example.derscustomlistview;

public class kisiClass {
    String kisiad;
    int kisino;
    int kisifoto;

    public kisiClass(String kisiad, int kisino, int kisifoto) {
        this.kisiad = kisiad;
        this.kisino = kisino;
        this.kisifoto = kisifoto;
    }

    public String getKisiad() {
        return kisiad;
    }

    public void setKisiad(String kisiad) {
        this.kisiad = kisiad;
    }

    public int getKisino() {
        return kisino;
    }

    public void setKisino(int kisino) {
        this.kisino = kisino;
    }

    public int getKisifoto() {
        return kisifoto;
    }

    public void setKisifoto(int kisifoto) {
        this.kisifoto = kisifoto;
    }
}
